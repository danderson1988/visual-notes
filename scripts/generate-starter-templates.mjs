// Regenerates src/starter-templates.ts from the real .canvas files in
// templates-src/ — run automatically before typecheck/build/dev (see
// package.json) so the generated module can never drift out of sync with
// its source templates. To add, remove, or edit a starter template, just
// add/remove/edit the .canvas file in templates-src/ (open it in Visual
// Notes or native Canvas like any other board) and rebuild.
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = fileURLToPath(new URL('..', import.meta.url));
const SRC_DIR = path.join(ROOT, 'templates-src');
const OUT_FILE = path.join(ROOT, 'src', 'starter-templates.ts');

const files = fs.readdirSync(SRC_DIR)
  .filter(f => f.endsWith('.canvas'))
  .sort((a, b) => a.localeCompare(b)); // stable, OS-independent ordering

const templates = files.map(file => {
  const name = file.slice(0, -'.canvas'.length);
  const json = fs.readFileSync(path.join(SRC_DIR, file), 'utf8');
  try {
    JSON.parse(json);
  } catch (err) {
    throw new Error(`templates-src/${file} is not valid JSON: ${err.message}`);
  }
  return { name, json };
});

const entries = templates
  .map(t => `  { name: ${JSON.stringify(t.name)}, json: ${JSON.stringify(t.json)} },`)
  .join('\n');

const out = `// AUTO-GENERATED by scripts/generate-starter-templates.mjs — do not hand-edit.
// Source of truth is templates-src/*.canvas; edit those (or add/remove
// files there) and rebuild to regenerate this file.
//
// Each entry's "json" is the verbatim contents of a .canvas board file.
// Nothing is written to the vault until the user explicitly picks one in
// the template picker, at which point it is installed to _Templates/ (once)
// and a fresh board is spawned from it like any other template.

export interface StarterTemplate { name: string; json: string; }

export const STARTER_TEMPLATES: StarterTemplate[] = [
${entries}
];
`;

fs.writeFileSync(OUT_FILE, out, 'utf8');
console.log(`Generated src/starter-templates.ts from ${templates.length} template(s) in templates-src/.`);
